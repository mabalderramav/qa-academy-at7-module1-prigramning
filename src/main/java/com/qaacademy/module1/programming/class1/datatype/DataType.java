package com.qaacademy.module1.programming.class1.datatype;

public class DataType {
    public static void main(String[] args) {

        // Declarar y asignar variable
        byte myNum = 100;
        System.out.println(myNum);

        // Declarar variable
        short myNum1;

        // Asignar variable
        myNum1 = 5000;
        System.out.println(myNum1);

        int myNum3 = 100000;
        System.out.println(myNum3);

        long myNum4 = 15000000000L;
        System.out.println(myNum4);

        float myNum5 = 6.86f;
        System.out.println(myNum5);

        double myNum6 = 19.3698d;
        System.out.println(myNum6);

        String greeting = "Hello world";
        System.out.println(greeting);

        boolean isJavaFun = true;
        System.out.println(isJavaFun);

        boolean isFridayToday = false;
        System.out.println(isFridayToday);

        char myGrade = 'D';
        System.out.println(myGrade);
    }
}
